const int MOD = 1e9 + 7;

template <int mod>
struct ModInt {
  int x;

  ModInt() : x(0) {}

  ModInt(int64_t y) : x(y >= 0 ? y % mod : (mod - (-y) % mod) % mod) {}

  ModInt &operator+=(const ModInt &p) {
    if ((x += p.x) >= mod) x -= mod;
    return *this;
  }

  ModInt &operator-=(const ModInt &p) {
    if ((x += mod - p.x) >= mod) x -= mod;
    return *this;
  }

  ModInt &operator*=(const ModInt &p) {
    x = (int)(1LL * x * p.x % mod);
    return *this;
  }

  ModInt &operator/=(const ModInt &p) {
    *this *= p.inverse();
    return *this;
  }

  ModInt operator-() const { return ModInt(-x); }

  ModInt operator+(const ModInt &p) const { return ModInt(*this) += p; }

  ModInt operator-(const ModInt &p) const { return ModInt(*this) -= p; }

  ModInt operator*(const ModInt &p) const { return ModInt(*this) *= p; }

  ModInt operator/(const ModInt &p) const { return ModInt(*this) /= p; }

  bool operator==(const ModInt &p) const { return x == p.x; }

  bool operator!=(const ModInt &p) const { return x != p.x; }

  ModInt inverse() const {
    int a = x, b = mod, u = 1, v = 0, t;
    while (b > 0) {
      t = a / b;
      swap(a -= t * b, b);
      swap(u -= t * v, v);
    }
    return ModInt(u);
  }

  ModInt pow(int64_t n) const {
    ModInt ret(1), mul(x);
    while (n > 0) {
      if (n & 1) ret *= mul;
      mul *= mul;
      n >>= 1;
    }
    return ret;
  }

  friend ostream &operator<<(ostream &os, const ModInt &p) { return os << p.x; }

  friend istream &operator>>(istream &is, ModInt &a) {
    int64_t t;
    is >> t;
    a = ModInt<mod>(t);
    return (is);
  }

  static constexpr int get_mod() { return mod; }
};
using mint = ModInt<MOD>;

const int X = 2e5;
mint fact[X+1];
mint nCk(ll n, ll k) { return fact[n] / fact[k] / fact[n - k]; }

void pre() {
  fact[0] = fact[1] = 1;
  for (ll i = 2; i <= X; i++) {
    fact[i] = fact[i - 1] * i;
  }
}

int n, k;
void solve() {
  cin >> n >> k;
  // notice that even one 0 makes a1&a2...&an = 0

  mint ans = 0;
  auto getEvenOnes=[&]() {
    mint res = 0;
    for(int i=0; i<n; i+=2) { // choose number of ones, must be even otherwise ^ > &
      res += nCk(n, i);
    }
    return res;
  };
  mint cEvenOnes = getEvenOnes();

  if(n&1) {
    ans = cEvenOnes + 1; // all 1s
    ans = ans.pow(k); // number if A is up to 2^k, meaning there are k possible arrangements, multiply them
  } else {
    // even case
    // & > ^  only when number of 1's is even
    // reduce to odd case?
    // if prefix & > prefix odd -> rest k-prefix bits can be whatever

    mint cEqual = 1; // combinations where & = ^

    for(int i=0; i<=k; i++) {
      int r = k-i-1;
      // make & > ^
      // 2^r ^ n
      ans += cEqual * (mint(2).pow(r)).pow(n);
      // add new equal combinations
      cEqual *= cEvenOnes; // even ones make & >= ^
    }
  }

  cout << ans << en;
}

int main() {
  pre();
  fast;
  int t;
  cin >> t;
  while(t--)
    solve();

}

