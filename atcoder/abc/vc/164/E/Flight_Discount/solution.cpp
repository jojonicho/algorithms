typedef tuple<int, int> Edge;
typedef tuple<ll, int, int> State; // cost, discounted, v

void solve() {
  int n, m;
  cin >> n >> m;

  vt<vt<Edge>> G(n);

  while(m--) {
    int u, v, c;
    cin >> u >> v >> c, u--, v--;
    G[u].pb({c, v});
  }

  priority_queue<State, vt<State>, greater<State>> pq;
  pq.push({0, 0, 0});

  ll INF = 2e14 + 5;
  vt<vt<ll>> dp(n, vt<ll>(2, INF));

  while(!pq.empty()) {
    auto [cu, isDiscounted, u] = pq.top(); pq.pop();
    if(cu > dp[u][isDiscounted])
      continue;

    for(auto [cv, v]: G[u]) {
      ll c = cu + cv;
      if(c < dp[v][isDiscounted]) {
        dp[v][isDiscounted] = c;
        pq.push({c, isDiscounted, v});
      }

      c = cu + cv/2;
      if(!isDiscounted && c < dp[v][1]) {
        dp[v][1] = c;
        pq.push({c, 1, v});
      }
    }
  }

  cout << dp[n-1][1] << en;
}

int main() {
  fast;
  int t=1;
  //cin >> t;
  while(t--)
    solve();
}

