const ll INF = 1e18 + 10;
const int N = 65;
ll A[N];

int n;
ll x;

ll ceil(ll a, ll b) { return (a + b - 1) / b; }

map<ll, ll> dp;
ll dfs(ll x, int idx) {
  if (dp.find(x) != dp.end()) return dp[x];
  if (idx == n || A[idx - 1] > x) return INF;

  ll big = A[idx], small = A[idx - 1];

  ll underCnt = x / big, under = big * underCnt, underNeed = x - under;
  ll overCnt = ceil(x, big), over = big * overCnt, overNeed = over - x;

  ll underAns = min(underCnt, dfs(under, idx + 1)) + underNeed / small;
  ll overAns = min(overCnt, dfs(over, idx + 1)) + overNeed / small;

  return dp[x] = min(underAns, overAns);
}

int main() {
  fast;
  cin >> n >> x;
  for (int i = 0; i < n; i++) cin >> A[i];
  dp[0] = 0;  // 0 coins of x = 0
  cout << dfs(x, 1);
}